package Assignment02;

public class Operation1unary {
    public static void main(String[] args) {
        int age=50;
        System.out.println("age= :" + ++age);
        System.out.println("age= :" + age++);
        System.out.println("age=: " + --age);
        //System.out.println("age=:" + age);/

        int age1=9;
        System.out.println("age1= :"+ --age1);
        System.out.println("age1= :"+ age1);

int age2=10;
        System.out.println("age2= :"+ age2--);
        System.out.println("age2= :"+ age2);


    }

}
